namespace GenComm\Service\Http\Response;

/**
 * Class Response
 * @package GenComm\Service\Http\Response
 */
class Response
{
    /**
     * @var
     */
    private $status;

    /**
     * @var
     */
    private $result;

    /**
     * @return mixed
     */
    public function getStatus()
    {
        return $this->status;
    }

    /**
     * @param $status
     * @return $this
     */
    public function setStatus($status)
    {
        $this->status = $status;

        return $this;
    }

    /**
     * @return mixed
     */
    public function getResult()
    {
        return $this->result;
    }

    /**
     * @param $result
     * @return $this
     */
    public function setResult($result)
    {
        $this->result = $result;

        return $this;
    }
}
